
public class SanPham {
	//Định nghĩa các thuộc tính
	int maSP;
	String tenSP;
	String loaiSP;
	String anhSP; // tên file ảnh
	//Hàm tạo
	public SanPham() {
		super();
	}
	public SanPham(int maSP, String tenSP, String loaiSP, String anhSP) {
		// khởi tạo giá trị ban đầu cho các thuộc tính nếu có ở đây
		super();
		this.maSP = maSP;
		this.tenSP = tenSP;
		this.loaiSP = loaiSP;
		this.anhSP = anhSP;
	}
	// Các getter, setter
	

}
